// boundaryStagger spreads simultaneous unmute / activate-slot firings at
// section / phrase boundaries across a window of ticks. Without this,
// every role's struct-* control net activates its next slot on the
// exact same tick — a "blast" re-entry. 12 ticks ≈ 3 beats at PPQ=4:
// enough slots to fully separate the typical 10–12 roles + stinger
// targets in an arrangement (drum slots, bass, harmony variants, hits),
// while keeping every role inside the same bar as the boundary.
// mute-track firings are NOT staggered (silence has no transient).
const boundaryStagger = 12

// Section defines a segment of a song structure.
type Section struct {
	Name    string
	Steps   int
	Active  map[string]bool     // net IDs that should be audible
	Phrases map[string][]string // role -> phrase variant sequence, e.g. {"kick": ["A","A","B","A"]}
}

// DefaultPhrases returns phrase patterns for a given section type.
func DefaultPhrases(sectionName string) []string {
	switch sectionName {
	case "intro", "outro":
		return []string{"A", "A"}
	case "verse":
		return []string{"A", "A", "B", "A"}
	case "chorus", "drop":
		return []string{"A", "B", "A", "B"}
	case "bridge", "breakdown":
		return []string{"C", "C"}
	case "buildup", "pre-chorus":
		return []string{"A", "B"}
	case "solo":
		return []string{"A", "B", "C", "A"}
	default:
		return []string{"A", "A"}
	}
}

// SongTemplate defines a sequence of sections.
type SongTemplate struct {
	Name     string
	Sections []Section
	// SlotMap is populated by expandVariants: role -> [sectionIdx][phraseIdx] -> global slot index.
	// -1 means the role is inactive in that phrase slot.
	SlotMap map[string][][]int
}

// Track roles used in templates. These map to the music net IDs
// generated by Compose (kick, snare, hihat, bass, melody, harmony, arp, clap).
var drumRoles = map[string]bool{"kick": true, "snare": true, "hihat": true, "clap": true}

// noVariantRoles are roles that should not get phrase variants — they just
// play a single looping pattern and get muted/unmuted per section.
var noVariantRoles = map[string]bool{"snare": true}

// sectionWithPhrases creates a Section with default phrase patterns for all active roles.
func sectionWithPhrases(name string, steps int, active map[string]bool) Section {
	phrases := make(map[string][]string)
	pattern := DefaultPhrases(name)
	for role := range active {
		if noVariantRoles[role] {
			continue
		}
		phrases[role] = pattern
	}
	return Section{Name: name, Steps: steps, Active: active, Phrases: phrases}
}

