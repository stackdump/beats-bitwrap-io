// SongStructure builds linear control nets that implement a song structure.
// For roles with slot variants, it creates one control net per role that
// uses activate-slot to switch between slot nets at phrase boundaries.
// A stop net fires stop-transport at the end.
// Returns the list of net IDs that should start muted.
func SongStructure(proj *pflow.Project, template *SongTemplate, musicNets []string) []string {
	totalSteps := 0
	for _, sec := range template.Sections {
		totalSteps += sec.Steps
	}

	var initialMutes []string

	// Build set of base roles that have slot variants
	slotRoles := make(map[string]bool)
	for _, netId := range musicNets {
		if nb, ok := proj.Nets[netId]; ok && nb.RiffGroup != "" {
			slotRoles[nb.RiffGroup] = true
		}
	}

	// Build a deterministic stagger offset per control-net target so unmute /
	// activate-slot events at the same boundary land on different ticks.
	var staggerKeys []string
	seenForStagger := make(map[string]bool)
	for _, netId := range musicNets {
		nb, ok := proj.Nets[netId]
		if !ok {
			continue
		}
		key := netId
		if nb.RiffGroup != "" {
			key = nb.RiffGroup
		} else if slotRoles[netId] {
			continue
		}
		if seenForStagger[key] {
			continue
		}
		seenForStagger[key] = true
		staggerKeys = append(staggerKeys, key)
	}
	sort.Strings(staggerKeys)
	staggerOf := make(map[string]int, len(staggerKeys))
	for i, k := range staggerKeys {
		staggerOf[k] = i % boundaryStagger
	}

	// Build one control net per role (using SlotMap)
	processedRoles := make(map[string]bool)
	for _, netId := range musicNets {
		nb, ok := proj.Nets[netId]
		if !ok {
			continue
		}

		if nb.RiffGroup != "" {
			role := nb.RiffGroup
			if processedRoles[role] {
				continue
			}
			processedRoles[role] = true

			slotMap := template.SlotMap[role]
			ctrl := roleControlNet(role, slotMap, template, totalSteps, staggerOf[role])
			proj.Nets[fmt.Sprintf("struct-%s", role)] = ctrl

			// Determine initial mutes: all slot nets start muted except the first active one
			firstSlot := -1
			if len(slotMap) > 0 && len(slotMap[0]) > 0 {
				firstSlot = slotMap[0][0]
			}
			for _, nId := range musicNets {
				if nb2, ok := proj.Nets[nId]; ok && nb2.RiffGroup == role {
					slotIdx := ExtractSlotIndex(nId, role)
					if slotIdx != firstSlot || firstSlot < 0 {
						initialMutes = append(initialMutes, nId)
					}
				}
			}
		} else if !slotRoles[netId] {
			// Stingers are one-shot accents and must never be driven by
			// section structure — skip the control net entirely and
			// keep them muted from the start.
			if pflow.IsStingerNet(netId, nb) {
				initialMutes = append(initialMutes, netId)
				continue
			}
			// Non-variant net — use original section-boundary control
			ctrl := linearControlNet(netId, template, totalSteps, staggerOf[netId])
			proj.Nets[fmt.Sprintf("struct-%s", netId)] = ctrl

			if !template.Sections[0].Active[netId] {
				initialMutes = append(initialMutes, netId)
			}
		}
	}

	// Stop net
	proj.Nets["struct-stop"] = linearStopNet(totalSteps)

	// Store structure metadata for timeline display
	sections := make([]pflow.StructureSection, len(template.Sections))
	for i, sec := range template.Sections {
		ss := pflow.StructureSection{Name: sec.Name, Steps: sec.Steps}
		if sec.Phrases != nil {
			ss.Phrases = make(map[string][]string)
			for role, phrases := range sec.Phrases {
				ss.Phrases[role] = phrases
			}
		}
		sections[i] = ss
	}
	proj.Structure = sections

	return initialMutes
}

// ExtractSlotIndex parses a slot index from a net ID like "kick-3" -> 3.
func ExtractSlotIndex(netId, riffGroup string) int {
	suffix := netId[len(riffGroup)+1:]
	idx := 0
	for _, c := range suffix {
		if c >= '0' && c <= '9' {
			idx = idx*10 + int(c-'0')
		}
	}
	return idx
}

// roleControlNet creates a single linear control net for a role.
// It uses activate-slot at phrase boundaries to switch between slot nets,
// and mute-track when the role becomes inactive in a section.
func roleControlNet(role string, slotMap [][]int, template *SongTemplate, totalSteps, stagger int) *pflow.NetBundle {
	net := petri.NewPetriNet()
	controlBindings := make(map[string]*pflow.ControlBinding)

	cx, cy, radius := ringLayout(totalSteps)

	// Build the schedule of active slots at each phrase boundary
	prevSlot := -1
	if len(slotMap) > 0 && len(slotMap[0]) > 0 {
		prevSlot = slotMap[0][0]
	}

	pos := 0
	for si, sec := range template.Sections {
		phrases := sec.Phrases[role]
		if len(phrases) == 0 {
			phrases = []string{"A"}
		}
		phraseLen := sec.Steps / len(phrases)

		for pi := range phrases {
			phraseStart := pos + pi*phraseLen
			if phraseStart == 0 {
				continue // initial state handled by initialMutes
			}

			curSlot := -1
			if si < len(slotMap) && pi < len(slotMap[si]) {
				curSlot = slotMap[si][pi]
			}

			if curSlot != prevSlot {
				// Stagger the activate-slot — but never the mute, and never
				// past the next phrase or the end of the chain.
				bindPos := phraseStart
				if curSlot >= 0 && stagger > 0 {
					maxOff := totalSteps - 1 - phraseStart
					if maxOff > phraseLen-1 {
						maxOff = phraseLen - 1
					}
					off := stagger
					if off > maxOff {
						off = maxOff
					}
					if off < 0 {
						off = 0
					}
					bindPos = phraseStart + off
				}
				tLabel := fmt.Sprintf("t%d", bindPos)
				if curSlot >= 0 {
					// Activate new slot (mutes all others in riff group, unmutes target)
					controlBindings[tLabel] = &pflow.ControlBinding{
						Action:    "activate-slot",
						TargetNet: fmt.Sprintf("%s-%d", role, curSlot),
					}
				} else {
					// Role becomes inactive — mute the outgoing slot
					controlBindings[tLabel] = &pflow.ControlBinding{
						Action:    "mute-track",
						TargetNet: fmt.Sprintf("%s-%d", role, prevSlot),
					}
				}
				prevSlot = curSlot
			}
		}
		pos += sec.Steps
	}

	// Build linear chain: totalSteps places + sink, totalSteps transitions
	for i := 0; i < totalSteps; i++ {
		initial := 0.0
		if i == 0 {
			initial = 1
		}
		angle := float64(i) / float64(totalSteps) * 2 * math.Pi
		x := cx + radius*0.7*math.Cos(angle)
		y := cy + radius*0.7*math.Sin(angle)
		net.AddPlace(fmt.Sprintf("p%d", i), initial, nil, x, y, nil)
	}

	// Sink place
	sinkAngle := float64(totalSteps) / float64(totalSteps) * 2 * math.Pi
	sinkX := cx + radius*0.7*math.Cos(sinkAngle)
	sinkY := cx + radius*0.7*math.Sin(sinkAngle)
	net.AddPlace(fmt.Sprintf("p%d", totalSteps), 0, nil, sinkX, sinkY, nil)

	for i := 0; i < totalSteps; i++ {
		tLabel := fmt.Sprintf("t%d", i)
		angle := (float64(i) + 0.5) / float64(totalSteps) * 2 * math.Pi
		tx := cx + radius*math.Cos(angle)
		ty := cy + radius*math.Sin(angle)
		net.AddTransition(tLabel, "", tx, ty, nil)

		net.AddArc(fmt.Sprintf("p%d", i), tLabel, 1.0, false)
		net.AddArc(tLabel, fmt.Sprintf("p%d", i+1), 1.0, false)
	}

	track := pflow.Track{Channel: 1}
	nb := pflow.NewNetBundle(net, track, make(map[string]*pflow.MidiBinding))
	nb.Role = "control"
	nb.ControlBindings = controlBindings
	return nb
}

// linearControlNet creates a linear chain of places (not a ring).
// At section boundaries where the target net's active state changes,
// it emits mute-track or unmute-track control bindings.
func linearControlNet(targetNet string, template *SongTemplate, totalSteps, stagger int) *pflow.NetBundle {
	net := petri.NewPetriNet()
	controlBindings := make(map[string]*pflow.ControlBinding)

	cx, cy, radius := ringLayout(totalSteps)

	// Linear chain: totalSteps places, totalSteps-1 transitions
	// (last place is a sink — token stays there)
	for i := 0; i < totalSteps; i++ {
		initial := 0.0
		if i == 0 {
			initial = 1
		}
		angle := float64(i) / float64(totalSteps) * 2 * math.Pi
		x := cx + radius*0.7*math.Cos(angle)
		y := cy + radius*0.7*math.Sin(angle)
		pLabel := fmt.Sprintf("p%d", i)
		net.AddPlace(pLabel, initial, nil, x, y, nil)
	}

	// Add sink place at end
	sinkAngle := float64(totalSteps) / float64(totalSteps) * 2 * math.Pi
	sinkX := cx + radius*0.7*math.Cos(sinkAngle)
	sinkY := cx + radius*0.7*math.Sin(sinkAngle)
	net.AddPlace(fmt.Sprintf("p%d", totalSteps), 0, nil, sinkX, sinkY, nil)

	// Build section boundary positions
	wasActive := template.Sections[0].Active[targetNet]
	pos := 0
	for si := 1; si < len(template.Sections); si++ {
		pos += template.Sections[si-1].Steps
		isActive := template.Sections[si].Active[targetNet]
		if isActive != wasActive {
			// Stagger only the unmute side, and never past the next section.
			bindPos := pos
			if isActive && stagger > 0 {
				nextSec := totalSteps
				if si+1 < len(template.Sections) {
					nextSec = pos + template.Sections[si].Steps
				}
				maxOff := nextSec - pos - 1
				off := stagger
				if off > maxOff {
					off = maxOff
				}
				if off < 0 {
					off = 0
				}
				bindPos = pos + off
			}
			tLabel := fmt.Sprintf("t%d", bindPos)
			if isActive {
				controlBindings[tLabel] = &pflow.ControlBinding{
					Action:    "unmute-track",
					TargetNet: targetNet,
				}
			} else {
				controlBindings[tLabel] = &pflow.ControlBinding{
					Action:    "mute-track",
					TargetNet: targetNet,
				}
			}
			wasActive = isActive
		}
	}

	// Transitions: one per step, connecting p[i] -> p[i+1]
	for i := 0; i < totalSteps; i++ {
		tLabel := fmt.Sprintf("t%d", i)
		angle := (float64(i) + 0.5) / float64(totalSteps) * 2 * math.Pi
		tx := cx + radius*math.Cos(angle)
		ty := cy + radius*math.Sin(angle)
		net.AddTransition(tLabel, "", tx, ty, nil)

		net.AddArc(fmt.Sprintf("p%d", i), tLabel, 1.0, false)
		net.AddArc(tLabel, fmt.Sprintf("p%d", i+1), 1.0, false)
	}

	track := pflow.Track{Channel: 1}
	nb := pflow.NewNetBundle(net, track, make(map[string]*pflow.MidiBinding))
	nb.Role = "control"
	nb.ControlBindings = controlBindings
	return nb
}

// linearStopNet creates a linear chain where the final transition
// fires a stop-transport control binding.
func linearStopNet(totalSteps int) *pflow.NetBundle {
	net := petri.NewPetriNet()
	controlBindings := make(map[string]*pflow.ControlBinding)

	cx, cy, radius := ringLayout(totalSteps + 1)

	// totalSteps+1 places, totalSteps transitions
	for i := 0; i <= totalSteps; i++ {
		initial := 0.0
		if i == 0 {
			initial = 1
		}
		angle := float64(i) / float64(totalSteps+1) * 2 * math.Pi
		x := cx + radius*0.7*math.Cos(angle)
		y := cy + radius*0.7*math.Sin(angle)
		pLabel := fmt.Sprintf("p%d", i)
		net.AddPlace(pLabel, initial, nil, x, y, nil)
	}

	for i := 0; i < totalSteps; i++ {
		tLabel := fmt.Sprintf("t%d", i)
		angle := (float64(i) + 0.5) / float64(totalSteps+1) * 2 * math.Pi
		tx := cx + radius*math.Cos(angle)
		ty := cy + radius*math.Sin(angle)
		net.AddTransition(tLabel, "", tx, ty, nil)

		net.AddArc(fmt.Sprintf("p%d", i), tLabel, 1.0, false)
		net.AddArc(tLabel, fmt.Sprintf("p%d", i+1), 1.0, false)

		// Last transition fires stop-transport
		if i == totalSteps-1 {
			controlBindings[tLabel] = &pflow.ControlBinding{
				Action: "stop-transport",
			}
		}
	}

	track := pflow.Track{Channel: 1}
	nb := pflow.NewNetBundle(net, track, make(map[string]*pflow.MidiBinding))
	nb.Role = "control"
	nb.ControlBindings = controlBindings
	return nb
}
